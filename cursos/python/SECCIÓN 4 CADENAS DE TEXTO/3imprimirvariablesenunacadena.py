# Imprimir variables en una cadena

nombre = "Antonio"

print("Buenos días " + nombre)

# .format
nombre2 = "Pepe"
edad = 18 

print ("Buenos días {}, feliz {} cumpleaños".format(nombre2,edad))

resultado = 10 / 3

print(resultado)
print("El resultado es {r:1.3f}".format(r=resultado))

# f-strings
nombre3 = "Joselete"
edad = 25
print(f'Buenos días {nombre3}, feliz {edad} cumpleaños')

