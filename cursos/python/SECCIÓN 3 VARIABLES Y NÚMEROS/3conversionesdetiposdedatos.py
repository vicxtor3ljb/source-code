# Conversiones de tipos de datos

numero = 5
cadena = str(numero)

print(numero)
print(cadena)

print(type(numero))
print(type(cadena))

cadena2 = '25'
numero2 = int(cadena2)

print(cadena2)
print(numero2)

print(type(cadena2))
print(type(numero2))

numero3 = 4

suma = numero2 + numero3

print(suma)
print(type(suma))

cadena3 = '25.7'
numerodecimal2 = float(cadena3)

print(cadena3)
print(numerodecimal2)

print(type(cadena3))
print(type(numerodecimal2))

numerodecimal3 = 2.3 
sumadecimal = numerodecimal2 + numerodecimal3

print("Esto es suma decimal %f", sumadecimal)
print(type(sumadecimal))


