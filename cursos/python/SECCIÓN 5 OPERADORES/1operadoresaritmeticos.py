# Operadores aritméticos (+,-,*,/,%,**,//)
# suma, resta, multiplicación, división, resto, exponente y el cociente

numero1 = 10
numero2 = 5

suma = numero1 + numero2
print("La suma es: ", suma)

resta = numero1 - numero2
print("La resta es: ", resta)

multiplicacion = numero1 * numero2 
print("La multiplicación es: ", multiplicacion)

division = numero1 / numero2
print("La division es: ", division)

numero3 = 5
numero4 = 2
division = numero3 / numero4
print("La division es: ", division)

# operador para el resto #
resto = numero3 % numero4
print("El resto es: ", resto)


# operador para el cociente //
cociente = numero3 // numero4
print("El cociente es: ", cociente)

# operador exponente
exponente = 2 ** 3
print("El exponente de 2 elevado a 3 es: ", exponente)



